#'separate facilities; once on an Illumina NovaSeq 6000 and once on an
#'Illumina NextSeq 500. These libraries are combined in this data set, per cell
#'library information is not in the public data repository. Reads were aligned
#'and counted using CellRanger 3.0.2.
#'
#'scRNA-seq for 4 additional SSc-IID patients and 4 additional healthy controls
#'is available but not included in this data set.
