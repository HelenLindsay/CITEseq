#'@details
#'CITE-seq of lung tissue samples from two patients with systemic sclerosis
#'interstitial lung disease (SSc-ILD) and one healthy control.  SScILD lung
#'tissue was obtained at lung transplantation.  Normal lung tissue was obtained
#'from organ donors.
#'
#'Samples were taken from the upper and lower lobe lung of the patients and one
